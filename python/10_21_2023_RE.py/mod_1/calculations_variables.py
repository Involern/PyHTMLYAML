## Order of operations;
# Follows PEMDAS, parenthesis, exponent, multiplication, 
# division, addition, subtraction
# Anything within parantheses is done first.

result_1 = 3 + 4 * 20
result_2 = (3 + 4) * 20 # Uses parentheses

print(result_1, result_2)

